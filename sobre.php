    <h1>Music House</h1>
    <p>
    O projeto <strong>Music House</strong> foi desenvolvido como parte da disciplina de Banco de Dados 2 do Instituto Federal - Campus Hortolândia. O objetivo principal era desenvolver um banco de dados robusto com 12 ou mais tabelas utilizando o programa <strong>pgAdmin4</strong> do <strong>PostgreSQL</strong>.
    </p>

    <br>

    <h2>Integração com a API da Deezer</h2>
    <p>
    Um dos recursos mais interessantes do projeto é a integração com a API da <strong>Deezer</strong>. Com essa integração, somos capazes de retornar o nome do artista e/ou banda, juntamente com uma pequena imagem de ilustração e suas <strong>Top 10 músicas</strong>mais tocadas no streaming em questão.
    </p>

